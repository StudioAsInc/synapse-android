package com.synapse.social.studioasinc.model;

public class Post {
    private String key;
    private String uid;
    private String post_text;
    private String post_type;
    private String post_hide_views_count;
    private String post_region;
    private String post_hide_like_count;
    private String post_hide_comments_count;
    private String post_visibility;
    private String post_disable_favorite;
    private String post_disable_comments;
    private String publish_date;
    private String post_image;

    public Post() {
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getPost_text() {
        return post_text;
    }

    public void setPost_text(String post_text) {
        this.post_text = post_text;
    }

    public String getPost_type() {
        return post_type;
    }

    public void setPost_type(String post_type) {
        this.post_type = post_type;
    }

    public String getPost_hide_views_count() {
        return post_hide_views_count;
    }

    public void setPost_hide_views_count(String post_hide_views_count) {
        this.post_hide_views_count = post_hide_views_count;
    }

    public String getPost_region() {
        return post_region;
    }

    public void setPost_region(String post_region) {
        this.post_region = post_region;
    }

    public String getPost_hide_like_count() {
        return post_hide_like_count;
    }

    public void setPost_hide_like_count(String post_hide_like_count) {
        this.post_hide_like_count = post_hide_like_count;
    }

    public String getPost_hide_comments_count() {
        return post_hide_comments_count;
    }

    public void setPost_hide_comments_count(String post_hide_comments_count) {
        this.post_hide_comments_count = post_hide_comments_count;
    }

    public String getPost_visibility() {
        return post_visibility;
    }

    public void setPost_visibility(String post_visibility) {
        this.post_visibility = post_visibility;
    }

    public String getPost_disable_favorite() {
        return post_disable_favorite;
    }

    public void setPost_disable_favorite(String post_disable_favorite) {
        this.post_disable_favorite = post_disable_favorite;
    }

    public String getPost_disable_comments() {
        return post_disable_comments;
    }

    public void setPost_disable_comments(String post_disable_comments) {
        this.post_disable_comments = post_disable_comments;
    }

    public String getPublish_date() {
        return publish_date;
    }

    public void setPublish_date(String publish_date) {
        this.publish_date = publish_date;
    }

    public String getPost_image() {
        return post_image;
    }

    public void setPost_image(String post_image) {
        this.post_image = post_image;
    }
}
